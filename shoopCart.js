    function startTimerShirt() {
      var presentTime = document.getElementById('shirt_timer').innerHTML;
      var timeArray = presentTime.split(/[:]+/);
      var m = timeArray[0];
      var s = checkSecond((timeArray[1] - 1));
      if(s==59){m=m-1}
      if(m==-1){return;}
      
      document.getElementById('shirt_timer').innerHTML =
        m + ":" + s;
      setTimeout(startTimerShirt, 1000);
    }


